/* 처방 ID/이름으로 찾기 */
function findFormula(query){
  if(!query) return null;
  const q = String(query).trim().replace(/[\s·()]+/g,'');
  for(const f of HG_FORMULAS){
    if(f.id === q) return f;
    if(f.aliases.some(a => a.replace(/[\s·()]+/g,'') === q)) return f;
  }
  return null;
}

