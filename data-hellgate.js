/* 정규화: 입력 토큰 → key (없으면 토큰 그대로) */
function normalizeHerb(token){
  if(!token) return '';
  const t = String(token).trim().replace(/[\s·,()/\-]+/g,'');
  if(!t) return '';
  for(const key in HERB_ALIASES){
    if(HERB_ALIASES[key].some(a => a.replace(/[\s·]+/g,'') === t)) return key;
  }
  return t;
}

