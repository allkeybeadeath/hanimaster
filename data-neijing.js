/* ─── 매일 1편 결정론적 선택 ───────────────────────────────────────
 * KST(UTC+9) 자정 기준 dayIndex. 같은 날짜의 모든 사용자가 같은 명언.
 * 시간대를 KST 로 고정하는 이유: 본 앱 사용자가 한국 본과 2학년 학습자
 * 그룹으로, 한국 자정에 명언이 바뀌는 것이 직관에 맞음.
 */
function neijingDayIndexKST(now){
  const t = (now || new Date()).getTime();
  // UTC ms 에 +9h 더하면 KST 자정에 day index 가 바뀜
  return Math.floor((t + 9 * 3600 * 1000) / 86400000);
}

function pickDailyAphorism(){
  if(!Array.isArray(NEIJING_APHORISMS) || NEIJING_APHORISMS.length === 0) return null;
  const i = neijingDayIndexKST() % NEIJING_APHORISMS.length;
  return NEIJING_APHORISMS[i];
}

/* 다음 명언 갱신까지 남은 ms */
function msUntilNextAphorism(){
  const now = Date.now();
  const cur = neijingDayIndexKST(new Date(now));
  return ((cur + 1) * 86400000) - 9 * 3600 * 1000 - now;
}

if(typeof window !== 'undefined'){
  window.NEIJING_APHORISMS = NEIJING_APHORISMS;
  window.pickDailyAphorism = pickDailyAphorism;
  window.neijingDayIndexKST = neijingDayIndexKST;
  window.msUntilNextAphorism = msUntilNextAphorism;
}
